//Знайти добуток усіх цілих чисел від 15 до 35

let product = 1; // Змінна для збереження добутку, початкове значення - 1

for (let i = 15; i <= 35; i++) {
  product *= i; // Множимо поточне число i на попередній добуток та зберігаємо результат в змінній product
}

console.log("Добуток усіх цілих чисел від 15 до 35 має "+ product);